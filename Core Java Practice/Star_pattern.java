// Decending Star Pattern //
class collection_framework{
    public static void main(String... args){
        for (int i=5;i>=1;i--){
            for(int j=1;j<=i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
// Assending Star Pattern //
public class Star_pattern {
    public static void main(String[] args)
    {
        for(int i=1;i<=5;i++){

             for(int y=1;y<=i;y++){
                System.out.print("*");
            }

            System.out.println();
        }
    }
}
